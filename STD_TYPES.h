#ifndef STD_TYPES_H
#define STD_TYPES_H

typedef unsigned char             u8 ; /*(Type : unsigned char -> (from 0 to 255) )*/
typedef signed char               s8 ; /*(Type : signed char -> (from -128 to 127) )*/
typedef unsigned short int        u16; /*(Type : unsigned short integer -> (from 0 to 65535) )*/
typedef signed short int          s16; /*(Type : signed short integer -> (from -32768 to 32767) )*/
typedef unsigned long int         u32; /*(Type : unsigned long integer -> (from 0 to 4294967295) )*/
typedef signed long int           s32; /*(Type : signed long integer -> (from -2147483648 to 2147483647) )*/
typedef unsigned long long int    u64; /*(Type : unsigned long long integer -> (from 0 to 18446744073709551615) )*/
typedef signed long long int      s64; /*(Type : signed long long integer -> (from -9223372036854775808 to 9223372036854775807) )*/
typedef float                     f32; /*(Type : float -> (from 1.1754945e-38 to 3.402823E+38) )*/
typedef double                    f64; /*(Type : double -> (from 1.7976931348623157E-308 to 1.7976931348623157E+308) )*/



#endif
